def team_name(english: str) -> str:
    """Return the team name in the current language, falling back to English."""
    lang = st.session_state.get("lang", "en")
    if lang == "en":
        return english
    row = TEAMS.get(english)
    if not row:
        return english
    return row.get(lang, english)


